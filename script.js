// Smooth reveal animation for hero section
window.addEventListener('load', () => {
    const heroContent = document.querySelector('.hero-content');
    const heroVisual = document.querySelector('.hero-visual');
    
    if (heroContent && heroVisual) {
        heroContent.style.opacity = '0';
        heroContent.style.transform = 'translateX(-50px)';
        heroVisual.style.opacity = '0';
        heroVisual.style.transform = 'translateX(50px)';
        
        setTimeout(() => {
            heroContent.style.transition = 'opacity 1s ease, transform 1s ease';
            heroVisual.style.transition = 'opacity 1s ease, transform 1s ease';
            
            heroContent.style.opacity = '1';
            heroContent.style.transform = 'translateX(0)';
            heroVisual.style.opacity = '1';
            heroVisual.style.transform = 'translateX(0)';
        }, 300);
    }
});

// Add loading animation for images
document.addEventListener('DOMContentLoaded', () => {
    const images = document.querySelectorAll('img');
    images.forEach(img => {
        // Set initial state
        img.style.opacity = '0';
        img.style.transition = 'opacity 0.5s ease';
        
        // Handle successful load
        img.addEventListener('load', function() {
            console.log('Image loaded:', this.src);
            this.style.opacity = '1';
        });
        
        // Handle load error
        img.addEventListener('error', function() {
            console.error('Image failed to load:', this.src);
            this.style.opacity = '1'; // Show anyway, might be a path issue
        });
        
        // If image is already cached/loaded, show it immediately
        if (img.complete && img.naturalHeight !== 0) {
            img.style.opacity = '1';
        }
    });
});


